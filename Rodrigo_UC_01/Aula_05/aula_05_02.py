num = [10,2,3,5,6,20,50,77,4,15]

soma = sum (num)
maior = max (num)
menor = min (num)
media = sum (num) / len (num) # a Função SUM soma todos os numereros e a Len conta a quantidad de numeros 

num.sort() # exibe a lista em ordem crescente 
print (num)
num.sort(reverse=True)
print(num)


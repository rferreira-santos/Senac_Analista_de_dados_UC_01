# Escreva um programa que leia um valor inteiro e apresente os resultados do quadrado e do cubo do valor lido.

# Entrada de Dados

n = int(input("Digite um valor: "))

# Processamento dos Dados| eu achei dificil, mas e basicamente a raiz quadrada imputida: e "N" e o numero imputido e a variáveis são a saida das soma:

quadrado = n * n # quadrado = n**2
cubo = n * n * n # cubo = n**3

# Saída de Dados

print(f"O quadrado de {n} é: {quadrado}")

print(f"O cubo de {n} é: {cubo}")